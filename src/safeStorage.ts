// Безопасная запись в localStorage. При переполнении квоты браузер бросает
// QuotaExceededError прямо в месте записи (часто внутри useEffect) — без обёртки
// это роняло бы сохранение молча. Здесь ловим ошибку и один раз предупреждаем
// пользователя, советуя сделать резервную копию и освободить место.

let warned = false;

export function safeSetItem(key: string, value: string): boolean {
  try {
    localStorage.setItem(key, value);
    warned = false; // запись прошла — снимаем флаг, чтобы предупредить снова при новой проблеме
    return true;
  } catch {
    if (!warned) {
      warned = true;
      window.alert(
        'Не удалось сохранить данные: хранилище браузера переполнено.\n\n' +
          'Скачайте резервную копию (кнопка «Копия») и удалите часть документов или записей, ' +
          'чтобы освободить место. Последнее изменение могло не сохраниться.',
      );
    }
    return false;
  }
}
